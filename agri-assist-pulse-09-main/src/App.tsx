import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route, Navigate } from "react-router-dom";
import { LanguageProvider } from "@/contexts/LanguageContext";
import { AuthProvider, useAuth } from "@/contexts/AuthContext";
import Index from "./pages/Index";
import MarketForecaster from "./pages/MarketForecaster";
import PestDetection from "./pages/PestDetection";
import IrrigationPredictor from "./pages/IrrigationPredictor";
import CarbonEstimator from "./pages/CarbonEstimator";
import ActivityHistory from "./pages/ActivityHistory";
import Login from "./pages/Login";
import NotFound from "./pages/NotFound";

const queryClient = new QueryClient();

const ProtectedRoute = ({ children }: { children: React.ReactNode }) => {
  const { isAuthenticated, loading } = useAuth();
  
  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-center">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-primary mx-auto mb-4"></div>
          <p className="text-muted-foreground">Loading...</p>
        </div>
      </div>
    );
  }
  
  if (!isAuthenticated) {
    return <Navigate to="/login" replace />;
  }
  
  return <>{children}</>;
};

const App = () => (
  <QueryClientProvider client={queryClient}>
    <AuthProvider>
      <LanguageProvider>
        <TooltipProvider>
          <Toaster />
          <Sonner />
          <BrowserRouter>
            <Routes>
              <Route path="/login" element={<Login />} />
              <Route path="/" element={<ProtectedRoute><Index /></ProtectedRoute>} />
              <Route path="/market-forecaster" element={<ProtectedRoute><MarketForecaster /></ProtectedRoute>} />
              <Route path="/pest-detection" element={<ProtectedRoute><PestDetection /></ProtectedRoute>} />
              <Route path="/irrigation-predictor" element={<ProtectedRoute><IrrigationPredictor /></ProtectedRoute>} />
              <Route path="/carbon-estimator" element={<ProtectedRoute><CarbonEstimator /></ProtectedRoute>} />
              <Route path="/activity-history" element={<ProtectedRoute><ActivityHistory /></ProtectedRoute>} />
              <Route path="*" element={<NotFound />} />
            </Routes>
          </BrowserRouter>
        </TooltipProvider>
      </LanguageProvider>
    </AuthProvider>
  </QueryClientProvider>
);

export default App;